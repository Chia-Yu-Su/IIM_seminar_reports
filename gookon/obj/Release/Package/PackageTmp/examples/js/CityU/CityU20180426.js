function svgToInit() {
    var svgObject = document.getElementById('object').contentDocument;
    var test = svgObject.getElementsByTagName('svg');
    var de = test[0];

    de.currentScale = 1;
    de.currentTranslate.x = 0;
    de.currentTranslate.y = 0;
}
function zoom(type) {

    var svgObject = document.getElementById('object').contentDocument;
    var test = svgObject.getElementsByTagName('svg');


    var de = test[0];
    var oldScale = de.currentScale;
    var oldTranslate = { x: de.currentTranslate.x, y: de.currentTranslate.y };
    var s = 1.5;
    if (type == 'in')
        de.currentScale *= s;
    if (type == 'out')
        de.currentScale /= s;

    var vp_width = window.innerWidth;
    var vp_height = window.innerHeight;

    vp_width = 1000;
    vp_height = 600;

    de.currentTranslate.x = vp_width / 2 - (de.currentScale / oldScale) * (vp_width / 2 - oldTranslate.x);
    de.currentTranslate.y = vp_height / 2 - (de.currentScale / oldScale) * (vp_height / 2 - oldTranslate.y);
}

function pan(type) {

    var svgObject = document.getElementById('object').contentDocument;
    var test = svgObject.getElementsByTagName('svg');

    var ct = test[0].currentTranslate;
    var t = 30;
    if (type == 'right')
        ct.x += t;
    if (type == 'down')
        ct.y += t;
    if (type == 'left')
        ct.x -= t;
    if (type == 'up')
        ct.y -= t;
}

